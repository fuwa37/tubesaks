    private void okKacaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okKacaActionPerformed
        // TODO add your handling code here:
        KacaKusen.listKaca.clear();
        DefaultTableModel model = (DefaultTableModel) tabelKaca.getModel();
        int i, j, id, banyak = 0;
        float pa = 0, le;
        for (i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0) == null) {
                break;
            }
            id = (int) model.getValueAt(i, 0);
            pa = (float) model.getValueAt(i, 1);
            le = (float) model.getValueAt(i, 2);
            banyak = (int) model.getValueAt(i, 3);
            Kaca kaca = new Kaca(id, pa, le, KacaKusen.DataKaca.kepadatan);
            KacaKusen.listKaca.put(kaca, banyak);
        }
        banyakKaca.setText(Integer.toString(KacaKusen.banyakKaca()));
        effKaca.setText(Float.toString(KacaKusen.efektifKaca()));
        //banyakText.setText(Float.toString(KacaKusen.luasKaca));
        //effText.setText(Float.toString(KacaKusen.DataKaca.area));
        //banyakKaca.setText(Integer.toString(banyak));
        //effKaca.setText(Float.toString(pa));
        dataKaca.setVisible(false);
        kacaDialog.setVisible(true);
        kacaDialog.setAlwaysOnTop(true);
        hasilKaca.setLocationRelativeTo(null);
        hasilKaca.setSize(370, 220);
        hasilKaca.setVisible(true);
        hasilKaca.setAlwaysOnTop(true);
    }//GEN-LAST:event_okKacaActionPerformed

