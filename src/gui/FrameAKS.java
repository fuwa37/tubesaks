    private void okKusenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okKusenActionPerformed
        // TODO add your handling code here:
        KacaKusen.listKusen.clear();
        DefaultTableModel model = (DefaultTableModel) tabelKusen.getModel();
        int i, j, id, banyak = 0;
        float pa = 0, le;
        for (i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0) == null) {
                break;
            }
            id = (int) model.getValueAt(i, 0);
            pa = (float) model.getValueAt(i, 1);
            le = (float) model.getValueAt(i, 2);
            banyak = (int) model.getValueAt(i, 3);
            Kusen kusen = new Kusen(id, pa, le, KacaKusen.DataKusen.bahan);
            KacaKusen.listKusen.put(kusen, banyak);
        }
        banyakKusen.setText(Integer.toString(KacaKusen.banyakKusen()));
        effKusen.setText(Float.toString(KacaKusen.efektifKusen()));
        dataKusen.setVisible(false);
        hasilKusen.setLocationRelativeTo(null);
        hasilKusen.setSize(370, 220);
        hasilKusen.setVisible(true);
        hasilKusen.setAlwaysOnTop(true);
    }//GEN-LAST:event_okKusenActionPerformed

